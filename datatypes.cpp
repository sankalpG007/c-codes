#include<iostream>
using namespace std;

int main(){
    int a;
    a=12;

    cout<<"size of int " <<sizeof(a)<<endl;

    float b;
    cout<<"size of float " <<sizeof(b)<<endl;

    double c;
    cout<<"size of double " <<sizeof(c)<<endl;

    bool d;
    cout<<"size of bool " <<sizeof(d)<<endl;


    return 0;
}